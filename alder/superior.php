	WANE is limited to 8.33 percent the surface measure of the board on the SUPERIOR face. The reverse side of the board the aggregate wane will not exceed 25 percent the width by 75 percent the length, or 33 percent the width by 50 percent the length. Wane may be divided, showing on both board edges.<br />
	<br />
	The aggregate inches of PITH length will not exceed 2 times the surface feet of the board.<br />
	<br />
	The aggregate inches of SPLIT length will not exceed 2 times the surface feet of the board. Splits diverging more than 1inch (25.40 mm) per foot, in length, are not admitted, except when 12 inches (30.48 cm) or shorter and within one lineal foot from the end of the board, leaving at least 50 percent clear wood and not less than 25 percent sound wood in aggregate.<br />
